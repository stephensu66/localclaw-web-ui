function getLanguage() {
  const query = new URLSearchParams(window.location.search);
  const lang = query.get('lang');
  return LANGUAGES.some((item) => item.value === lang) ? lang : 'en';
}

function withLanguage(lang) {
  const url = new URL(window.location.href);
  url.searchParams.set('lang', lang);
  return `${url.pathname}${url.search}${url.hash}`;
}

function renderParagraph(text) {
  const p = document.createElement('p');
  const pattern = /(https?:\/\/[^\s]+|[A-Z0-9._%+-]+@[A-Z0-9.-]+\.[A-Z]{2,})/gi;
  let cursor = 0;
  text.replace(pattern, (match, _value, offset) => {
    if (offset > cursor) {
      p.appendChild(document.createTextNode(text.slice(cursor, offset)));
    }

    const link = document.createElement('a');
    link.href = match.startsWith('http') ? match : `mailto:${match}`;
    link.textContent = match;
    if (match.startsWith('http')) {
      link.target = '_blank';
      link.rel = 'noreferrer';
    }
    p.appendChild(link);
    cursor = offset + match.length;
    return match;
  });

  if (cursor < text.length) {
    p.appendChild(document.createTextNode(text.slice(cursor)));
  }
  return p;
}

function renderList(items) {
  const ul = document.createElement('ul');
  items.forEach((item) => {
    const li = document.createElement('li');
    li.textContent = item;
    ul.appendChild(li);
  });
  return ul;
}

function renderLinks(links) {
  const list = document.createElement('div');
  list.className = 'legal-links';
  links.forEach((item) => {
    const link = document.createElement('a');
    link.href = item.href;
    link.textContent = item.label;
    list.appendChild(link);
  });
  return list;
}

function renderLegalPage() {
  const page = document.body.dataset.legalPage;
  const language = getLanguage();
  const content = CONTENT[page]?.[language] ?? CONTENT[page]?.en;
  const root = document.getElementById('legal-root');

  if (!content || !root) {
    return;
  }

  document.documentElement.lang = content.lang;
  document.title = `${content.title} | LocalClaw`;

  const top = document.createElement('div');
  top.className = 'legal-top';

  const back = document.createElement('a');
  back.href = '/';
  back.textContent = content.back;
  top.appendChild(back);

  const switcher = document.createElement('nav');
  switcher.className = 'legal-lang';
  switcher.setAttribute('aria-label', 'Language');
  LANGUAGES.forEach((item) => {
    const link = document.createElement('a');
    link.href = withLanguage(item.value);
    link.textContent = item.label;
    if (item.value === language) {
      link.className = 'is-active';
    }
    switcher.appendChild(link);
  });
  top.appendChild(switcher);

  const article = document.createElement('article');
  article.className = 'legal-card';

  const title = document.createElement('h1');
  title.textContent = content.title;
  article.appendChild(title);

  const meta = document.createElement('p');
  meta.className = 'meta';
  meta.textContent = content.updated;
  article.appendChild(meta);

  const notice = document.createElement('div');
  notice.className = 'legal-notice';
  notice.textContent = content.notice;
  article.appendChild(notice);

  content.intro?.forEach((text) => article.appendChild(renderParagraph(text)));

  content.sections.forEach((section) => {
    const heading = document.createElement('h2');
    heading.textContent = section.title;
    article.appendChild(heading);

    section.paragraphs?.forEach((text) => article.appendChild(renderParagraph(text)));

    if (section.list) {
      article.appendChild(renderList(section.list));
    }

    if (section.links) {
      article.appendChild(renderLinks(section.links));
    }
  });

  root.replaceChildren(top, article);
}

renderLegalPage();
